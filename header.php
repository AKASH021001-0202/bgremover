            const myDiv = document.getElementById('myDiv');
            const myHeader = document.getElementById('myHeader');
            const toggleButton = document.getElementById('toggleButton');

            // Function to toggle the class and update the state
            function toggleDarkMode() {
                myDiv.classList.toggle('home-three-dark');
                myDiv.classList.toggle('home-three-light');
                myHeader.classList.toggle('header--dark');
                myHeader.classList.toggle('header--light');

                // Store the state in a cookie
                const isDarkMode = myDiv.classList.contains('home-three-dark');
                document.cookie =` darkMode = ${isDarkMode}`;
            }

            // Apply the initial state based on the cookie
            const cookieValue = document.cookie
                .split('; ')
                .find(row => row.startsWith('darkMode='));

            if (cookieValue) {
                const isDarkMode = cookieValue.split('=')[1] === 'true';
                if (isDarkMode) {
                    toggleDarkMode();
                }
            }

            // Add a click event listener to the button
            toggleButton.addEventListener('click', toggleDarkMode);

